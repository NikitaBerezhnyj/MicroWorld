function love.conf(t)
    t.title = "MicroWorld"      -- Назва вікна
    t.window.width = 800        -- Ширина вікна
    t.window.height = 600       -- Висота вікна
    t.window.resizable = false  -- Зробити вікно незмінним (можна поставити true для зміни розміру)
    t.window.fullscreen = false -- Вікно не в повноекранному режимі
end
